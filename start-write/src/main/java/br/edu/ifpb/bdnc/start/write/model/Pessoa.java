package br.edu.ifpb.bdnc.start.write.model;

import java.util.Date;
import java.util.Objects;

/**
 *
 * @author miolivc
 */
public class Pessoa {
    
    private String username;
    private Date birthDate;
    private String password;
    private String email;
    private String name;

    public Pessoa(String username, Date birthDate, String password, String email, String name) {
        this.username = username;
        this.birthDate = birthDate;
        this.password = password;
        this.email = email;
        this.name = name;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public Date getBirthDate() {
        return birthDate;
    }

    public void setBirthDate(Date birthDate) {
        this.birthDate = birthDate;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int hashCode() {
        int hash = 5;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Pessoa other = (Pessoa) obj;
        if (!Objects.equals(this.username, other.username)) {
            return false;
        }
        if (!Objects.equals(this.email, other.email)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Pessoa{" + "username=" + username + ", birthDate=" + birthDate +
            ", password=" + password + ", email=" + email + ", name=" + name + '}';
    }
    
}
